export function tType(typeLabel) {
  const key = TYPE_MAP[typeLabel];
  return key ? t(key) : (typeLabel || '-');
}

export function tStatus(statusLabel, speed) {
  if (statusLabel) {
    const key = STATUS_MAP[statusLabel];
    if (key) return t(key);
    return statusLabel;
  }
  return speed > 0.5 ? t('moving') : t('stopped');
}
